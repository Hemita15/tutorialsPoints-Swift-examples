import Foundation

import Cocoa

func ifelseif() -> () {
    
    let varA:Int = 10
    
    if varA == 20
        {
            print("varA is equal to 20")
    }
    else if varA == 40
    {
          print("varA is equal to 40")
    }
    else
    {
        print("none of the value is matching")
    }
    
    print("value of varA is \(varA)")
}
