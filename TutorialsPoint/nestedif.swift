import Foundation

import Cocoa

func nestedif() -> () {
    
    let varA = 20
    let varB = 30
    
    if varA == varB
    {
        print("varA is equal to varB")
    
    }
    if varA != varB
    {
        print("varA is not equal to varB")
    }
    
    print("value of varA is \(varA)")
    print("value of varB is \(varB)")
    
}

